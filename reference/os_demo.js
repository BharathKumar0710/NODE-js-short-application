// Gives the information about the environment and the operating system
const os = require('os');

// os platform
console.log(os.platform());

// CPU architecture
console.log(os.arch());

// CPU Core info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// total memory
console.log(os.totalmem());

// Home directory
console.log(os.homedir());

// up time
console.log(os.uptime());

